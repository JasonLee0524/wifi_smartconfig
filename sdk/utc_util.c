/*******************************************************************************
* Function    : utc_lrc8
* Description : 计算lrc8
* Input       : src   数据
                count 数据长度
* Output      : no
* Return      : 
*******************************************************************************/
u8 utc_lrc8(u8 *src, u16 count)
{
	u8 lrc_temp = 0;
	while(count--)
    {
		lrc_temp ^= *src++;
	}
	return lrc_temp;
}

static u8 crc_msb(u8 value)
{
    u8 i, crc;

    crc = value;
    /* 数据往左移了8位，需要计算8次 */
    for (i = 8; i > 0; --i)
    {
        if (crc & 0x80) /* 判断最高位是否为1 */
        {
            /* 最高位为1，不需要异或，往左移一位，然后与0x31异或 */
            /* 0x31(多项式：x8+x5+x4+1，100110001)，最高位不需要异或，直接去掉 */
            crc = (crc << 1) ^ 0x31;
        }
        else
        {
            /* 最高位为0时，不需要异或，整体数据往左移一位 */
            crc = (crc << 1);
        }
    }

    return crc;
}

static u8 crc_lsb(u8 value)
{
    u8 i, crc;

    crc = value;
    /* 同样需要计算8次 */
    for (i = 8; i > 0; --i)
    {
        if (crc & 0x01) /* 反序异或变成判断最低位是否为1 */
            /* 数据变成往右移位了 */
            /* 计算的多项式从0x31（0011 0001）变成了0x8C (1000 1100) */
            /* 多项式值，原来的最高位变成了最低位，原来的最低位变成最高位，8位数据高低位交换一下位置 */
            crc = (crc >> 1) ^ 0x8C;
        else
            crc = (crc >> 1);
    }

    return crc;
}

/*******************************************************************************
* Function    : utc_crc8_msb
* Description : 计算crc8 msb
* Input       : src   数据
                count 数据长度
* Output      : no
* Return      : 
*******************************************************************************/
u8 utc_crc8_msb(u8 *src, u8 count, u8 init_crc)
{
    u8 crc = init_crc; /* 计算的初始crc值 */

    while(count--)
    {
        crc ^= *src++;  /* 每次先与需要计算的数据异或,计算完指向下一数据 */  
        crc = crc_msb(crc);
    }

    return crc;
}

/*******************************************************************************
* Function    : utc_crc8_lsb
* Description : 计算crc8 lsb
* Input       : src   数据
                count 数据长度
* Output      : no
* Return      : 
*******************************************************************************/
u8 utc_crc8_lsb(u8 *src, u8 count, u8 init_crc)
{
    u8 crc = init_crc; /* 计算的初始crc值 */

    while(count--)
    {
        crc ^= *src++;  /* 每次先与需要计算的数据异或,计算完指向下一数据 */  
        crc = crc_lsb(crc);
    }

    return crc;
}

