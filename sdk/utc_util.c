/*******************************************************************************
* Function    : utc_memxcpy
* Description : 逆向数据拷贝
* Input       : des 逆向后的数据
                src 逆向前的数据
				count 数据长度
* Output      : no
* Return      : no
*******************************************************************************/
void utc_memxcpy(u8 *des,u8 *src,u32 count)
{
    u32 i;
    for(i=0; i<count; i++)
    {
        *(des+i) = *(src+count-i-1);
    }
}

/*******************************************************************************
* Function    : utc_asc_to_bcd
* Description : 将ASCII码转换为压缩BCD码
* Input       : des  BCD码数据
                src  ASCII码数据
                count 数据长度
* Output      : no
* Return      : 末尾不够补0x00,非ascii码填0x00   
*******************************************************************************/
s32 utc_asc_to_bcd(u8 *des,u8 *src,u32 count)
{
	u32 i;
	u8 high = 0,low = 0;
    
	for(i = 0; i < count; i++) 
	{
		//待转bcd码高Nibble
		if((*(src + i) >= 0x61) && (*(src + i) <= 0x66))      //range a~f
		{
			high = *(src + i) - 0x57;
		}
		else if((*(src + i) >= 0x41) && (*(src + i) <= 0x46))  //range A~F
		{
			high = *(src + i) - 0x37;
		}
		else if((*(src + i) >= 0x30) && (*(src + i) <= 0x39))  //range 0~9
		{
			high = *(src + i) - 0x30;   
		}
		else
		{
			high = 0x00 ;                                       //其他
		}

		//待转bcd码低Nibble
		i++;
		if(i < count)
		{
			if((*(src + i) >= 0x61) && (*(src + i) <= 0x66))    //range a~f
			{
				low = *(src + i) - 0x57;
			}
			else if((*(src + i) >= 0x41) && (*(src + i) <= 0x46)) //range A~F
			{   
				low = *(src + i) - 0x37;
			}
			else if((*(src + i) >= 0x30) && (*(src + i) <= 0x39))  //range 0~9
			{
				low = *(src + i) - 0x30;
			}
			else
			{
				low = 0x00 ;                                       //其他
			}
		}
		else
		{
			i--;                                                //预防255个时溢出出错
			low = 0x00 ;                                       //如果是奇数个末尾补0x00 
		}
		*(des + i/2) = (high << 4) | low;                      //合并BCD码
	}
	return OS_OK;
}


/*******************************************************************************
* Function    : utc_crc16
* Description : 计算crc16
* Input       : src   数据
                count 数据长度
* Output      : no
* Return      :   
*******************************************************************************/
u16 utc_crc16(u8 *src, u16 count)
{
    u8 crc_h = 0xFF;
    u8 crc_l = 0xFF;
    u32 i;
    
    while( count-- )
    {
        i = crc_l ^ *src++;
        crc_l = (u8)(crc_h ^ crc_high_tatle[i]);
        crc_h = crc_low_tatle[i];
    }
	
    return (u16)(crc_h << 8 | crc_l);
}


